#pragma once

///--------------------------------------------------------------------------
/// @brief    Interface that defines the application main  functionalities

///--------------------------------------------------------------------------
class IApp {
  public:
    ///--------------------------------------------------------------------------
    /// @brief    Virtual destructor
    ///--------------------------------------------------------------------------
    virtual ~IApp() = default;
    ///--------------------------------------------------------------------------
    /// @brief    Initializes the application
    ///--------------------------------------------------------------------------
    virtual void init() = 0;
    ///--------------------------------------------------------------------------
    /// @brief    Runs the application main loop
    ///--------------------------------------------------------------------------
    virtual void run() = 0;
    ///--------------------------------------------------------------------------
    /// @brief    Stops the application
    ///--------------------------------------------------------------------------
    virtual void stop() = 0;
};